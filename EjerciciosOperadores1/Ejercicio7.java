public class Ejercicio7 {

	public static void main(String[] args) {
		
		char x = 'v', y = 'V', z = ' ';
		System.out.println("La variable 'x' contiene una " + x);
		System.out.println("La variable 'y' contiene una " + y);
		//El espacio, obviamente, no se muestra
		System.out.println("La variable 'z' contiene un " + z + "(espacio)");

	}

}
