/*
 *	9.- Obtener la suma de todos los números entre 0 y 100 exceptuando aquellos que contengan un 7.
 * 
 */

public class Ejercicio9 {

	public static void main(String[] args) {
		
		String cadena = "";
		
		for( int i = 6; i <= 60; i = i+6) {
			
			cadena = cadena + i + " ";
		}
		System.out.println(cadena);
		
		

	}

}
